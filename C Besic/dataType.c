#include<stdio.h>

int main()
{
    int a=20; //here a is a integer type data
    float b=20.5; //here b is a float type data
    char c='S'; //here c is a character type data
    printf(" The value of a is %d\n",a); //for print interger we use "%d"
    printf(" The value of b is %f\n",b); //for print float we use "%f"
    printf(" The value of c is %c\n",c); //for print charcter we use "%c"
    return 0;
}